package com.example.rivera_multicalc;

public class Formulas1 {
    //Weight
    public double WFactivity(double Mass, double Acceleration) { return Mass * Acceleration;}
    //Power
    public double Pf2Activity(double Work, double Time) { return Work / Time;}

    //Pressure
    public double Prf2Activity(double Force, double Total) { return Force / Total;}

    //Average Speed
    public double zAsf2Activity(double Distance, double Time) { return Distance / Time;}}


